# Operasi Matematika

def tambah(bil1, bil2):
    hasil = bil1 + bil2
    print("hasil tambah dari\t: ",bil1, "\t","+", bil2, "=", hasil)

def pengurangan(bil1,bil2):
    hasil = bil1 - bil2
    print("hasil dari pengurangan\t: ", bil1,"\t", "-", bil2, "=", hasil)
def perkalian(bil1, bil2):
    hasil = bil1 * bil2
    print("hasil dari perkalian\t: ", bil1, "\t","*", bil2, "=", hasil)
def pembagian(bil1, bil2):
    hasil = bil1 / bil2
    print("hasil dari pembagian\t: ", bil1, "\t","/", bil2, "=", hasil)